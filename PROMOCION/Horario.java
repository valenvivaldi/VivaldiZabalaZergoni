public class Horario {
	private int nroJug;
	private int primHora;
	private int ultHora;
	
	public Horario(int j, int prim, int ult){
		nroJug=j;
		primHora=prim;
		ultHora=ult;
		}
	
	
	public int obtenerPrim () {
		
		return primHora;
		}
	
	
	public int obtenerUlt () {
		
		return ultHora;
		}
	
	
	public int obtenerNroJ () {
		
		return nroJug;
		}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}
